const BEST_PRACTICES = `
## Best Practices: Рекомендации по Низким Метрикам

### Ось A — Корректность ответа
- **f_EM < 0.3**: ответ систематически не совпадает с эталоном.
  → Проверить промпт в PromptBuilder: возможно слишком широкая или неточная инструкция.
  → Снизить temperature LLMCall (0.0–0.2 для точных задач).
  → Проверить, передаётся ли нужный контекст в LLMCall.
- **f_sim < 0.5**: семантически далёкие ответы.
  → Добавить few-shot примеры в промпт.
  → Убедиться что формат ответа явно указан.
- **f_F1 < 0.4**: не перекрываются ключевые токены.
  → Проверить, не теряется ли важная часть входных данных при фильтрации/ранжировании.

### Ось B — Грунтованность
- **f_faith < 0.5**: ответ содержит факты, не подтверждённые контекстом (галлюцинации).
  → Добавить явную инструкцию "отвечай только на основе предоставленного контекста".
  → Снизить temperature.
  → Рассмотреть добавление GroundingChecker или CitationFormatter узла.
- **f_contra < 0.7**: NLI обнаруживает противоречия.
  → Проверить качество исходного контекста.

### Ось C — Retrieval
- **f_recall@k < 0.4**: ключевые документы не попадают в выборку.
  → Увеличить k в ретривере.
  → Проверить качество эмбеддингов (попробовать более мощную модель).
  → Рассмотреть Hybrid Retriever (BM25 + dense).
- **f_ctx_prec < 0.5**: слишком много нерелевантного контекста.
  → Снизить k или добавить Reranker/Filter после ретривера.

### Ось D — Tool-Use
- **f_toolsel < 0.6**: агент выбирает неверные инструменты.
  → Улучшить описания инструментов в конфигурации AgentCall.
  → Добавить examples в системный промпт AgentCall.
- **f_planEff < 0.5**: слишком много лишних шагов.
  → Ограничить набор доступных инструментов (убрать ненужные).
  → Добавить явную инструкцию быть лаконичным.
- **f_tool_ok < 0.8**: инструменты часто падают.
  → Проверить доступность внешних API.
  → Добавить RetryGate для нестабильных инструментов.

### Ось E — Структура вывода
- **f_schema < 0.7**: вывод не соответствует схеме.
  → Добавить явное описание формата в промпт (JSON с примером).
  → Добавить Parser узел после LLMCall/AgentCall.
  → Рассмотреть structured output mode модели.
- **f_field < 0.6**: поля заполнены некорректно.
  → Проверить prompt инструкцию на предмет чёткого описания каждого поля.

### Ось F — Control-Flow
- **f_loop_budget < 0.5**: цикл часто упирается в лимит итераций.
  → Увеличить maxIterations в LoopGate.
  → Проверить stopCondition — возможно, он никогда не выполняется.
- **f_loop_term < 0.7**: цикл не завершается естественно.
  → Проверить корректность stopCondition.
  → Рассмотреть добавление явного exit-условия.

### Ось G — LLM-as-Judge
- **f_judge_ref < 0.6**: оценка по рубрике ниже стандарта.
  → Уточнить и детализировать промпт в LLMCall/AgentCall.
  → Добавить ролевую инструкцию и примеры ожидаемого качества.
- **f_judge_coh / f_judge_flu < 0.7**: плохая связность или читаемость.
  → Попробовать более мощную модель.
  → Добавить инструкцию по стилю ответа.

### Ось H — Safety
- **f_safe < 0.95**: обнаружен токсичный контент.
  → Добавить safety guardrail в системный промпт.
  → Рассмотреть LlamaGuard как отдельный фильтр.
- **f_consist < 0.7**: нестабильные ответы при повторных прогонах.
  → Снизить temperature.
  → Использовать seed если провайдер поддерживает.
`.trim();

